import re

text = "Сегодня 13.03.2025, вчера было 12.03.2025, завтра будет 14.03.2025"

pattern = r'\b(0?[1-9]|[12][0-9]|3[01])\.(0?[1-9]|1[0-2])\.(\.{2}|\d{4})\b'
# 0?[1-9] от 1 до 9 при этом может начинаться с 01...
# [12][0-9] - от 10 до 39
# 3[01] - 30 и 31
# 1[0-2] - 10,11,12
#\d{2} - два числа (2025 - 25)
#\d{4} - четыре числа
dates = re.findall(pattern, text)

for date in dates:
    print(f"{date[0]}.{date[1]}.{date[2]}")